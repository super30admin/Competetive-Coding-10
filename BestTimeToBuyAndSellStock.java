public class BestTimeToBuyAndSellStock {
    // TC: O(N) where N is length of prices array
    // SC: O(1)
    public int maxProfit(int[] prices) {
        int res = 0;
        for (int i = 0; i < prices.length - 1; i++) {
            if (prices[i] < prices[i + 1]) {
                res += prices[i + 1] - prices[i];
            }
        }
        return res;
    }
}
